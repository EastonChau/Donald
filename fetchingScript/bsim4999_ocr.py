# MAIN OCR PROCESSING FUNCTION (WITH VIDEO SKIPPING)
# ============================================================
def process_media_with_ocr(media_urls, post_id="", verbose=True):
    """
    Process media URLs - skip videos, perform OCR only on images
    Args:
        media_urls: list of media URLs
        post_id: optional post identifier for logging
        verbose: print progress messages
    Returns:
        tuple: (combined_ocr_text, successful_images_count, skipped_videos_count)
    """
    if not media_urls:
        return "", 0, 0

    ocr_texts = []
    successful_count = 0
    skipped_videos = 0

    # Limit number of media items processed
    urls_to_process = media_urls[:OCRConfig.MAX_IMAGES_PER_POST]

    if verbose and len(media_urls) > OCRConfig.MAX_IMAGES_PER_POST:
        print(f"    ℹ Limiting to first {OCRConfig.MAX_IMAGES_PER_POST} of {len(media_urls)} media items")

    for i, url in enumerate(urls_to_process, 1):
        if verbose:
            print(f"    📷 Media {i}/{len(urls_to_process)}")

        # Identify media type
        media_type = identify_media_type(url)

        if media_type == 'video':
            if verbose:
                print(f"      🎬 Skipping video content")
            skipped_videos += 1
            continue

        elif media_type == 'image':
            if verbose:
                print(f"      🖼 Processing image...")

            # Download image
            img = download_image(url)

            if img:
                # Extract text
                extracted = extract_text_from_image(img)

                if extracted:
                    ocr_texts.append(extracted)
                    successful_count += 1
                    if verbose:
                        print(f"      ✓ Extracted {len(extracted)} characters: {extracted[:80]}...")
                else:
                    if verbose:
                        print(f"      ⚠ No text found in image")
            else:
                if verbose:
                    print(f"      ❌ Failed to download image")

        else:
            if verbose:
                print(f"      ❓ Unknown media type, skipping")
            continue

        # Delay between images
        time.sleep(OCRConfig.DELAY_BETWEEN_IMAGES)

    # Combine all OCR results
    if ocr_texts:
        combined = " | ".join(ocr_texts)
        return combined, successful_count, skipped_videos
    else:
        return "", 0, skipped_videos

