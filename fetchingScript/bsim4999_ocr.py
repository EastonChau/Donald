# ANALYZE MEDIA TYPES FUNCTION
# ============================================================
def analyze_media_types(csv_file, media_col='media_urls', delimiter='|'):
    """
    Analyze media types in the CSV file before processing
    """
    print("="*70)
    print("🔍 MEDIA TYPE ANALYSIS")
    print("="*70)

    # Read CSV
    df = pd.read_csv(csv_file)

    total_images = 0
    total_videos = 0
    total_unknown = 0
    posts_with_images = 0
    posts_with_videos = 0
    posts_with_mixed = 0

    for idx, row in df.iterrows():
        media_cell = str(row[media_col]) if pd.notna(row[media_col]) else ""

        if media_cell and media_cell != 'nan':
            media_urls = [url.strip() for url in media_cell.split(delimiter) if url.strip()]

            has_image = False
            has_video = False

            for url in media_urls:
                media_type = identify_media_type(url)
                if media_type == 'image':
                    total_images += 1
                    has_image = True
                elif media_type == 'video':
                    total_videos += 1
                    has_video = True
                else:
                    total_unknown += 1

            if has_image and has_video:
                posts_with_mixed += 1
            elif has_image:
                posts_with_images += 1
            elif has_video:
                posts_with_videos += 1

    print(f"\n📊 Media Statistics:")
    print(f"  • Total media items: {total_images + total_videos + total_unknown}")
    print(f"    - Images: {total_images} ({total_images/(total_images+total_videos+total_unknown)*100:.1f}%)")
    print(f"    - Videos: {total_videos} ({total_videos/(total_images+total_videos+total_unknown)*100:.1f}%)")
    print(f"    - Unknown: {total_unknown}")

    print(f"\n📸 Posts by media type:")
    print(f"  • Posts with only images: {posts_with_images}")
    print(f"  • Posts with only videos: {posts_with_videos}")
    print(f"  • Posts with mixed (images + videos): {posts_with_mixed}")
    print(f"  • Total posts with images: {posts_with_images + posts_with_mixed}")
    print(f"  • Total posts with videos: {posts_with_videos + posts_with_mixed}")

    return {
        'total_images': total_images,
        'total_videos': total_videos,
        'posts_with_images': posts_with_images + posts_with_mixed,
        'posts_with_videos': posts_with_videos + posts_with_mixed
    }

