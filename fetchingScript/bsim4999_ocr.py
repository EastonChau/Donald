# IMAGE PREPROCESSOR
# ============================================================
def preprocess_image(img):
    """
    Preprocess image for better OCR accuracy
    """
    try:
        # Convert to RGB if necessary
        if img.mode not in ('L', 'RGB'):
            img = img.convert('RGB')

        # Resize large images for better OCR performance
        if img.size[0] > OCRConfig.MAX_IMAGE_SIZE[0] or img.size[1] > OCRConfig.MAX_IMAGE_SIZE[1]:
            img.thumbnail(OCRConfig.MAX_IMAGE_SIZE, Image.Resampling.LANCZOS)

        return img

    except Exception as e:
        print(f"      ⚠ Preprocessing error: {str(e)}")
        return img

# ============================================================
# OCR TEXT EXTRACTOR
# ============================================================
def extract_text_from_image(img):
    """
    Extract text from image using Tesseract OCR
    Returns: extracted text string
    """
    try:
        # Preprocess image
        img = preprocess_image(img)

        # Perform OCR
        extracted_text = pytesseract.image_to_string(img, config=OCRConfig.OCR_CONFIG)

        # Clean up extracted text
        extracted_text = extracted_text.strip()
        extracted_text = ' '.join(extracted_text.split())  # Normalize whitespace
        extracted_text = extracted_text.replace('\n', ' ').replace('\r', ' ')

        return extracted_text

    except Exception as e:
        print(f"      🔍 OCR error: {str(e)}")
        return ""

