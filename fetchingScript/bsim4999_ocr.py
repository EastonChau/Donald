# ENHANCED CSV PROCESSOR (WITH VIDEO DETECTION)
# ============================================================
def process_csv_with_ocr_enhanced(input_file,
                                   media_url_column='media_urls',
                                   text_column='original_text',
                                   output_file='output_with_ocr.csv',
                                   delimiter='|',
                                   verbose=True):
    """
    Process CSV file and perform OCR ONLY on images (skip videos)
    """

    print("="*70)
    print("🚀 ENHANCED OCR PROCESSING (SKIPPING VIDEOS)")
    print("="*70)

    # Read CSV file
    print(f"\n📂 Reading CSV file: {input_file}")
    try:
        df = pd.read_csv(input_file)
        print(f"✓ Loaded {len(df)} rows")
        print(f"  Columns: {list(df.columns)}")
    except Exception as e:
        print(f"❌ Error reading CSV: {str(e)}")
        return None

    # Check required columns
    if media_url_column not in df.columns:
        print(f"\n❌ Error: '{media_url_column}' column not found in CSV")
        print(f"  Available columns: {list(df.columns)}")
        return None

    # Check Tesseract installation
    try:
        tesseract_version = pytesseract.get_tesseract_version()
        print(f"\n✅ Tesseract OCR version: {tesseract_version}")
    except Exception as e:
        print(f"\n❌ Tesseract OCR not properly installed: {str(e)}")
        return None

    # Create new columns for OCR results
    df['ocr_text'] = ''
    df['ocr_success'] = ''
    df['images_processed'] = 0
    df['videos_skipped'] = 0
    df['media_types'] = ''
    df['combined_text'] = ''

    # Process each row
    successful_posts = 0
    total_images_processed = 0
    total_videos_skipped = 0
    posts_with_videos = 0

    print("\n" + "="*70)
    print("📸 PROCESSING MEDIA WITH OCR (SKIPPING VIDEOS)")
    print("="*70)

    for idx, row in df.iterrows():
        print(f"\n📝 Row {idx + 1}/{len(df)}")

        # Get media URLs
        media_cell = str(row[media_url_column]) if pd.notna(row[media_url_column]) else ""

        if media_cell and media_cell != 'nan':
            # Split URLs by delimiter
            media_urls = [url.strip() for url in media_cell.split(delimiter) if url.strip()]

            # Identify media types
            media_types = []
            images = []
            videos = []

            for url in media_urls:
                media_type = identify_media_type(url)
                media_types.append(media_type)
                if media_type == 'image':
                    images.append(url)
                elif media_type == 'video':
                    videos.append(url)

            # Store media types
            df.at[idx, 'media_types'] = ' | '.join(media_types)
            df.at[idx, 'videos_skipped'] = len(videos)

            if videos:
                posts_with_videos += 1
                print(f"  🎬 Found {len(videos)} video(s) - skipping OCR")

            if images:
                print(f"  🖼 Found {len(images)} image(s) - processing OCR")

                # Show post preview
                original_text = row[text_column] if text_column in df.columns else ""
                if original_text and len(str(original_text)) > 50:
                    print(f"  📝 Original text: {str(original_text)[:50]}...")
                elif original_text:
                    print(f"  📝 Original text: {str(original_text)}")

                # Process only images
                ocr_result, success_count, skipped = process_media_with_ocr(
                    images,
                    post_id=idx,
                    verbose=verbose
                )

                # Update DataFrame
                if ocr_result:
                    df.at[idx, 'ocr_text'] = ocr_result
                    df.at[idx, 'ocr_success'] = 'Yes'
                    successful_posts += 1
                    total_images_processed += success_count

                    # Combine original text with OCR text
                    if text_column in df.columns and pd.notna(row[text_column]):
                        original_text = str(row[text_column])
                        combined = f"{original_text} [OCR: {ocr_result}]"
                        df.at[idx, 'combined_text'] = combined
                    else:
                        df.at[idx, 'combined_text'] = ocr_result
                else:
                    df.at[idx, 'ocr_success'] = 'No text found' if success_count == 0 else 'No'
                    if text_column in df.columns:
                        df.at[idx, 'combined_text'] = str(row[text_column])

                df.at[idx, 'images_processed'] = success_count
                total_videos_skipped += skipped

            else:
                print(f"  ℹ No images found (only videos or unknown media)")
                df.at[idx, 'ocr_success'] = 'Only videos'
                if text_column in df.columns:
                    df.at[idx, 'combined_text'] = str(row[text_column])
        else:
            print(f"  ℹ No media in this post")
            df.at[idx, 'ocr_success'] = 'No media'
            if text_column in df.columns:
                df.at[idx, 'combined_text'] = str(row[text_column])

        # Delay between posts
        time.sleep(OCRConfig.DELAY_BETWEEN_POSTS)

    # Print summary
    print("\n" + "="*70)
    print("📊 OCR PROCESSING COMPLETE")
    print("="*70)
    print(f"Total posts processed: {len(df)}")
    print(f"Posts with images: {len(df[df['images_processed'] > 0])}")
    print(f"Posts with videos: {posts_with_videos}")
    print(f"Posts with successful OCR: {successful_posts}")
    print(f"Total images processed: {total_images_processed}")
    print(f"Total videos skipped: {total_videos_skipped}")
    print(f"OCR success rate: {(successful_posts / len(df[df['images_processed'] > 0]) * 100 if len(df[df['images_processed'] > 0]) > 0 else 0):.1f}%")

    # Save results
    print(f"\n💾 Saving results to: {output_file}")
    df.to_csv(output_file, index=False, encoding='utf-8-sig')
    print(f"✓ File saved successfully")

    return df

