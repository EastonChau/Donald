# IMAGE DOWNLOADER
# ============================================================
def download_image(image_url, max_retries=OCRConfig.MAX_RETRIES):
    """
    Download image from URL with retry logic
    Returns: PIL Image object or None if download fails
    """
    for attempt in range(max_retries):
        try:
            headers = {'User-Agent': OCRConfig.USER_AGENT}
            response = requests.get(image_url,
                                  headers=headers,
                                  timeout=OCRConfig.DOWNLOAD_TIMEOUT)
            response.raise_for_status()

            # Check content type
            content_type = response.headers.get('Content-Type', '').lower()
            if 'video' in content_type:
                print(f"      🎬 Skipping video content")
                return None

            # Open image from bytes
            img = Image.open(BytesIO(response.content))
            return img

        except requests.exceptions.Timeout:
            print(f"    ⏱ Timeout downloading {image_url[:50]}... (attempt {attempt + 1}/{max_retries})")
            time.sleep(2)
        except requests.exceptions.RequestException as e:
            print(f"    📡 Download error: {str(e)[:50]} (attempt {attempt + 1}/{max_retries})")
            time.sleep(2)
        except Exception as e:
            print(f"    ❌ Image processing error: {str(e)[:50]}")
            return None

    return None

