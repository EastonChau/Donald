# VIDEO DETECTION FUNCTION
# ============================================================
def is_video_url(url):
    """
    Detect if a URL points to video content
    Returns: True if video, False otherwise
    """
    if not url:
        return False

    url_lower = url.lower()

    # Check for video file extensions
    video_extensions = ['.mp4', '.mov', '.avi', '.mkv', '.webm', '.flv', '.wmv']
    if any(url_lower.endswith(ext) for ext in video_extensions):
        return True

    # Check for video platform patterns
    video_patterns = [
        'youtube.com/watch',
        'youtu.be/',
        'vimeo.com/',
        'tiktok.com/',
        'instagram.com/reel',
        'instagram.com/tv',
        'twitch.tv',
        'twitter.com/i/videos',
        '/video/',
        'videos/',
        'watch?v='
    ]

    if any(pattern in url_lower for pattern in video_patterns):
        return True

    # Check if URL contains video in path
    if '/video' in url_lower or '/videos' in url_lower:
        return True

    # Check for Twitter video patterns
    if 'pbs.twimg.com' in url_lower and 'video' in url_lower:
        return True

    # Check media key patterns - Twitter videos often have 'video' in media_key
    if 'video' in url_lower:
        # But be careful: sometimes 'video' appears in image URLs from Twitter
        # Only flag if it's explicitly a video URL
        if 'video' in url_lower and not ('jpg' in url_lower or 'jpeg' in url_lower or 'png' in url_lower):
            return True

    return False

def identify_media_type(media_url):
    """
    Identify if media URL is image or video
    Returns: 'image', 'video', or 'unknown'
    """
    if not media_url:
        return 'unknown'

    # Check for video patterns
    if is_video_url(media_url):
        return 'video'

    # Check for image patterns
    image_extensions = ['.jpg', '.jpeg', '.png', '.gif', '.webp', '.bmp']
    url_lower = media_url.lower()

    if any(url_lower.endswith(ext) for ext in image_extensions):
        return 'image'

    # Twitter CDN images typically have pbs.twimg.com/media/
    if 'pbs.twimg.com/media/' in url_lower and not 'video' in url_lower:
        return 'image'

    # If it's from Twitter CDN and not clearly video, assume it's an image
    if 'pbs.twimg.com' in url_lower:
        return 'image'

    return 'unknown'

