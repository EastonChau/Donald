class OCRConfig:
    """Configuration settings for OCR processing"""

    # Image download settings
    DOWNLOAD_TIMEOUT = 15  # seconds
    MAX_RETRIES = 3
    USER_AGENT = 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36'

    # OCR settings
    MAX_IMAGES_PER_POST = 5  # Process up to 5 images per post
    OCR_LANGUAGE = 'eng'  # Default language (eng, chi_sim, jpn, etc.)
    OCR_CONFIG = '--psm 6 -l eng'  # PSM 6 = assume uniform text block

    # Processing settings
    DELAY_BETWEEN_IMAGES = 0.5  # seconds
    DELAY_BETWEEN_POSTS = 1.0   # seconds

    # Image preprocessing
    MAX_IMAGE_SIZE = (2000, 2000)  # Resize large images for better performance

